export type SkylineKey =
  | 'new york'
  | 'london'
  | 'paris'
  | 'tokyo'
  | 'chicago'
  | 'sydney'
  | 'dubai'
  | 'los angeles'
  | 'san francisco'
  | 'default'

export function getSkylineKey(city: string): SkylineKey {
  const c = city.toLowerCase()
  if (c.includes('new york') || c.includes('manhattan')) return 'new york'
  if (c.includes('london')) return 'london'
  if (c.includes('paris')) return 'paris'
  if (c.includes('tokyo')) return 'tokyo'
  if (c.includes('chicago')) return 'chicago'
  if (c.includes('sydney')) return 'sydney'
  if (c.includes('dubai')) return 'dubai'
  if (c.includes('los angeles') || c.includes('la ')) return 'los angeles'
  if (c.includes('san francisco') || c.includes('sf ')) return 'san francisco'
  return 'default'
}

